//=============================================================================
// インスタンス生成
// Author : 唐﨑結斗
// 概要 : 3Dモデルを生成する
//=============================================================================
CTestStencil *CTestStencil::Create(void)
{
	// オブジェクトインスタンス
	CTestStencil *pTestStencil = nullptr;

	// メモリの解放
	pTestStencil = new CTestStencil;

	// メモリの確保ができなかった
	assert(pTestStencil != nullptr);

	// 数値の初期化
	pTestStencil->Init();

	// インスタンスを返す
	return pTestStencil;
}

