// Author : 唐﨑結斗
// 概要 : 2D描画を行う
//=============================================================================
void CTestStencil::Draw()
{// レンダラーのゲット
	CRenderer *pRenderer = CApplication::GetRenderer();

	// ステンシルマスクの設定
	pRenderer->SetStencilMask(1, D3DCMP_ALWAYS);

	// 描画
	CBG::Draw();
}
